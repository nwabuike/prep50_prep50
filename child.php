                    <h3>Demonstrable learning</h3>
                    <p>
                        This type of learning is called <b>“Demonstrable learning”</b> or learning by demonstration and engagement. It requires two things.. Interest aroused by the activities and Interaction generated by the teacher; all these sums up as engagement.
                    </p>
                    <p>But here is the problem, neither is easy to achieve</p>
                    <p>Driving engagement for children and matching their energy is very tiring and so stressful. Secondly, gaining their attention can only last for a very short while and way too short to get them to learn anything. So we are in a situation where we want to raise smart kids but the process is very tiring and stressful and we can only guarantee their attention only for a very short time.</p>
                    <p>This is exactly why our whole schooling system needs a radical change. It emphasizes attendance but our presence in class is far less significant in making us learn anything in class that the critical engagement we are talking about. Our education is dying, not just because the smart minds are no longer in the class teaching but because the teachers available lack the creativity and imagination to engage our children to the point where they can arouse their interest to learn new stuffs. The singular ability to craft new ideas with which to learn something over and over again is what helps kids learn as they grow. You have to repeat the tasks frequently but you also need creativity to make those repetitions engaging. This is exactly what our prep50 engage is designed to achieve.</p>
                    <p><b>So how do you shift from child rearing to child upbringing? </b></p>
                    <p>There is a model we use @ Prep50 to engineer interest and interaction and make any child learn anything, grow smarter and more intelligent.</p>
