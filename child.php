                    <h2 style="font-size: 20px;">Child Rearing vs Child Upbringing</h2>
                    <span class="btm-bar"></span>
                    <p>
                        Recently, while reading a brilliant book on early child development, I came across this fascinating line on a discovery made by some Harvard child psychologists. The noted that “Every child in their early years needs two basic forces/drives to be able to learn anything; Interest and Interaction”. Without which, they will turn out as hyper-active, stubborn, always distracted…you can fill in the rest. When I shared this new insight with a few friends, most of them commented that they had observed similar trends among their children. According to them, children who were effectively engaged in their early years with task after another and series of activities tend to be very smart and alert in school in their future studies and all round growth and development. Whereas; those who either lack interest in or someone to play and learn with, tend to be thick-headed or stubborn and not brilliant in school.
                    </p>
                    <p>So, why does constantly engaging our kids seem to make them far smarter and more brilliant children?</p>
                    <p>To understand why, you have to understand the difference between child rearing and child upbringing. </p>
                    <p>Anyone can rear children, you give birth, you provide food for the baby and after 3 months, you dump the baby in a nearby crèche where he/she is also fed and sent back home in the evening. But these basic survival needs are a tiny fraction of what human growth and development really is. Nurturing kids to develop their full potentials and capabilities as human requires massive effort in his or her upbringing.</p>
                    <p>Child upbringing is the way a child is raised or the range of lessons, instruction and teachings that was imparted on the child by his parents or guardian as they grow. These lessons and teachings are basically points of engagements for the child.</p>
                    <p>What this explanation is saying is that when you attempt child upbringing, you irreversibly start raising your child in a fundamentally different way which is characterized by constant engagement for the child.</p>
                    <p>The other day for example, I experienced one of these home tutors teaching the kids at home. It was a tough subject…tenses in English Language. Throughout the duration of the lesson, the tutor struggled to capture the attention of the child talk more of getting the child to learn the information he was pouring on him for the last 2hrs. I don’t believe the tutor is a lazy guy or a dumb fellow but the simple truth was that he couldn’t capture the two basic instincts needed in the child to drive learning which are interest and interaction. He could continue for another 2hours on the same topic using the same strategy of reeling out information upon another and I can guarantee you that he will achieve absolutely nothing. However, if he had gotten three different cups and labelled them ‘Present tense’, ‘Past Tense’, ‘Future tense’; then instead of writing examples on the whiteboard, he can make each students to write different examples on a piece of paper. They will read it loud and determine which cup the sentence that was read belongs to. If the child places the paper in the correct cup, that is if he was able to identify which tense was used in the sentence, he will score a point. Say, “Obi will come tomorrow” should be inserted into the future tense cup.</p>
